package springboothelloworld.helloworld.pojo;

public class IDCardAuthorization {
	private String isAuthorized=null;
	

	public IDCardAuthorization() {
		super();
	}

	public IDCardAuthorization(String isAuthorized) {
		super();
		this.isAuthorized = isAuthorized;
	}

	public String getIsAuthorized() {
		return isAuthorized;
	}

	public void setIsAuthorized(String isAuthorized) {
		this.isAuthorized = isAuthorized;
	}
	

}
